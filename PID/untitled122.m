function pushbutton1_Callback(hObject, eventdata, handles)
z=str2num(get(handles.edit1,'string')); 
w=str2num(get(handles.edit7,'string'));
a2=str2num(get(handles.edit4,'string'));
a1=str2num(get(handles.edit5,'string'));
a0=str2num(get(handles.edit6,'string'));
b0=str2num(get(handles.edit8,'string'));
s=tf('s');
N=5; %filter
%a1=2;
%a2=3;
%b0=1;
%k=0.15625; %gain
%Ki=
%Kd=8
%Ti=0.025;
%Td=119.975;
%Td=N/(1*2*z*w);
%Ti=a1-Td/N;
Ti=a1-1/(2*z*w);
set(handles.edit11, 'String', Ti);
Td=(a2/Ti)-1/(2*z*w);
set(handles.edit9, 'String', Td);
k=w*Ti/(2*z*b0);
set(handles.edit10, 'String', k);
num=[b0];
denom=[a2 a1 1];
Gp=tf(num,denom); %plant
H=[1];
Gc=k*(1+1/(Ti*s)+(Td*s)/(1+(Td*s/N)));%pid controller
Mc=feedback(Gc*Gp,H); %to get closed loop
handles.axes1;
step(Mc);
grid on

